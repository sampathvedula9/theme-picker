def apply_dunst(p):
    """Replace urgency color sections in dunstrc."""
    if not os.path.exists(DUNST_CONFIG):
        return False
    backup_file(DUNST_CONFIG, "dunst")

    with open(DUNST_CONFIG) as f:
        content = f.read()

    # Also update global frame_color
    content = re.sub(
        r'(^\s*frame_color\s*=\s*)".+?"',
        rf'\1"{p["blue"]}"',
        content,
        count=1,
        flags=re.MULTILINE,
    )

    # Replace urgency sections
    urgency_map = {
        "urgency_low": {
            "background": p["bg"],
            "foreground": p["fg"],
            "frame_color": p["fg_dim"],
        },
        "urgency_normal": {
            "background": p["bg"],
            "foreground": p["fg"],
            "frame_color": p["blue"],
        },
        "urgency_critical": {
            "background": p["bg"],
            "foreground": p["fg"],
            "frame_color": p["red"],
        },
    }

    for section, colors in urgency_map.items():
        for key, val in colors.items():
            pattern = rf'(\[{section}\].*?^\s*{key}\s*=\s*)".+?"'
            content = re.sub(pattern, rf'\1"{val}"', content, count=1,
                             flags=re.MULTILINE | re.DOTALL)

    with open(DUNST_CONFIG, "w") as f:
        f.write(content)
    return True


def apply_rofi(p):
    """Replace color variables in rofi config.rasi * {} block."""
    if not os.path.exists(ROFI_CONFIG):
        return False
    backup_file(ROFI_CONFIG, "rofi")

    with open(ROFI_CONFIG) as f:
        content = f.read()

    replacements = {
        "bg": hex_alpha(p["bg"], "dd"),
        "bg-alt": hex_alpha(p["bg_alt"], "ff"),
        "fg": hex_alpha(p["fg"], "ff"),
        "fg-dim": hex_alpha(p["fg_dim"], "ff"),
        "accent": hex_alpha(p["accent"], "ff"),
        "border-c": hex_alpha(p["fg_dim"], "ff"),
    }

    for var, val in replacements.items():
        # Match variable definition lines like "    bg:       #1e1e1edd;"
        pattern = rf'(^\s*{re.escape(var)}:\s*)#[0-9a-fA-F]{{6,8}};'
        content = re.sub(pattern, rf'\g<1>{val};', content, flags=re.MULTILINE)

    with open(ROFI_CONFIG, "w") as f:
        f.write(content)
    return True


def apply_swaync(p):
    """Replace @define-color block at top of SwayNC style.css."""
    if not os.path.exists(SWAYNC_CSS):
        return False
    backup_file(SWAYNC_CSS, "swaync")

    with open(SWAYNC_CSS) as f:
        content = f.read()

    new_header = f"""/* {p.get('_name', 'Custom')} theme for SwayNotificationCenter */

/* ── Base colors ── */
@define-color bg {p['bg']};
@define-color bg-dark {p['bg_dark']};
@define-color fg {p['fg']};
@define-color selection {p['bg_alt']};
@define-color comment {p['fg_dim']};
@define-color cyan {p['cyan']};
@define-color green {p['green']};
@define-color orange {p['orange']};
@define-color pink {p['pink']};
@define-color purple {p['purple']};
@define-color red {p['red']};
@define-color yellow {p['yellow']};"""

    # Replace everything from start up to (but not including) the first non-color CSS rule
    # Match: comments + @define-color lines at the start
    content = re.sub(
        r'\A(/\*.*?\*/\s*)*(/\*.*?\*/\s*)?(@define-color\s+\S+\s+#[0-9a-fA-F]+;\s*)+',
        new_header + "\n\n",
        content,
        count=1,
        flags=re.DOTALL,
    )

    with open(SWAYNC_CSS, "w") as f:
        f.write(content)
    return True


def apply_chadwm(p, theme_id):
    """Generate chadwm .h theme file and update #include in config.def.h."""
    if not os.path.exists(CHADWM_THEMES_DIR):
        return False
    backup_file(CHADWM_CONFIG, "chadwm")

    theme_file = os.path.join(CHADWM_THEMES_DIR, f"{theme_id}.h")
    backup_file(theme_file, "chadwm")

    content = f"""static const char black[]       = "{p['bg']}";
static const char white[]       = "{p['fg']}";
static const char gray2[]       = "{p['bg_alt']}"; // unfocused window border
static const char gray3[]       = "{p['fg_dim']}";
static const char gray4[]       = "{lighten(p['fg_dim'], 0.15)}";
static const char blue[]        = "{p['blue']}";  // focused window border
static const char green[]       = "{p['green']}";
static const char red[]         = "{p['red']}";
static const char orange[]      = "{p['orange']}";
static const char yellow[]      = "{p['yellow']}";
static const char pink[]        = "{p['purple']}";
static const char pinky[]       = "{p['pink']}";
static const char col_borderbar[]  = "{p['bg_dark']}"; // inner border
"""
    with open(theme_file, "w") as f:
        f.write(content)

    # Update #include in config.def.h
    if os.path.exists(CHADWM_CONFIG):
        with open(CHADWM_CONFIG) as f:
            config = f.read()

        # Comment out all theme includes, then add/uncomment the target one
        # First, comment out any active include
        config = re.sub(
            r'^(\s*)#include\s+"themes/(\w+)\.h"',
            r'\1//#include "themes/\2.h"',
            config,
            flags=re.MULTILINE,
        )
        # Check if this theme already has a commented include line
        include_pattern = rf'^(\s*)//#include\s+"themes/{re.escape(theme_id)}\.h"'
        if re.search(include_pattern, config, re.MULTILINE):
            config = re.sub(
                include_pattern,
                rf'\1#include "themes/{theme_id}.h"',
                config,
                flags=re.MULTILINE,
            )
        else:
            # Add new include after the last commented theme include
            config = re.sub(
                r'(^//#include "themes/\w+\.h"\s*\n)(?!.*?#include "themes/)',
                rf'\1#include "themes/{theme_id}.h"\n',
                config,
                count=1,
                flags=re.MULTILINE,
            )

        with open(CHADWM_CONFIG, "w") as f:
            f.write(config)

    return True


# ── Main ──────────────────────────────────────────────────────────────────────

def main():
    if len(sys.argv) < 2:
        print("Usage: apply-theme.py <theme-id> [--dry-run]")
        print(f"\nAvailable themes:")
        themes = sorted(f[:-5] for f in os.listdir(THEME_DIR) if f.endswith(".json"))
        for t in themes:
            print(f"  {t}")
        sys.exit(1)

    theme_id = sys.argv[1]
    dry_run = "--dry-run" in sys.argv

    theme = load_theme(theme_id)
    p = theme["palette"]
    p["_name"] = theme["name"]
    theme_name = theme["name"]

    # Create timestamped backup directory
    timestamp = datetime.now().strftime("%Y%m%d-%H%M%S")
    backup_subdir_base = f"{timestamp}_{theme_id}"
    # Override the backup_file function's subdir with timestamped one
    global BACKUP_DIR
    BACKUP_DIR = os.path.join(BASE_DIR, "backups", backup_subdir_base)
    os.makedirs(BACKUP_DIR, exist_ok=True)

    if dry_run:
        print(f"DRY RUN — would apply theme: {theme_name} ({theme_id})")
        print(f"Palette: {json.dumps(p, indent=2)}")
        return

    print(f"Applying theme: {theme_name} ({theme_id})")

    # Detect running WM
    hyprland_running = is_running("Hyprland")
    chadwm_running = is_running("chadwm")

    # Check which bar/shell is running under Hyprland
    dms_running = is_running("dms")
    noctalia_running = is_running("noctalia")

    results = []

    def _run_safe(cmd):
        """Run a command ignoring failures and timeouts."""
        try:
            subprocess.run(cmd, stderr=subprocess.DEVNULL, stdout=subprocess.DEVNULL, timeout=5)
        except (FileNotFoundError, subprocess.TimeoutExpired, subprocess.SubprocessError):
            pass

    # Always apply these
    if apply_dunst(p):
        results.append("Dunst")
        _run_safe(["pkill", "dunst"])
    if apply_rofi(p):
        results.append("Rofi")

    # Hyprland targets
    if hyprland_running:
        if apply_hyprland(p):
            results.append("Hyprland borders")
        if apply_swaync(p):
            results.append("SwayNC")
            _run_safe(["swaync-client", "-rs"])

    if dms_running or os.path.exists(DMS_THEMES_DIR):
        if apply_dms(p, theme_id, theme_name):
            results.append("DMS")

    if noctalia_running or os.path.exists(NOCTALIA_SCHEMES_DIR):
        if apply_noctalia(p, theme_id, theme_name):
            results.append("Noctalia")

    # chadwm targets
    if chadwm_running or os.path.exists(CHADWM_CONFIG):
        if apply_chadwm(p, theme_id):
            results.append("chadwm (rebuild needed: cd ~/.config/arco-chadwm/chadwm && sudo make clean install)")

    save_current(theme_id)

    print(f"\nApplied to: {', '.join(results)}")
    print(f"Backup saved to: {BACKUP_DIR}")

    if "chadwm" in str(results):
        print("\n⚠  chadwm: rebuild required!")
        print("   cd ~/.config/arco-chadwm/chadwm && sudo make clean install")
        print("   Then Super+Shift+R to reload")

    # Send notification
    _run_safe(["notify-send", "-a", "theme-picker", f"Theme: {theme_name}",
               f"Applied to: {', '.join(r.split(' (')[0] for r in results)}"])


if __name__ == "__main__":
    main()

# ── Helpers ───────────────────────────────────────────────────────────────────

def strip_hash(hex_color):
    """'#ff5555' → 'ff5555'"""
    return hex_color.lstrip("#")


def hex_alpha(hex_color, alpha="dd"):
    """'#ff5555' → '#ff5555dd'"""
    return hex_color + alpha


def is_running(process_name):
    """Check if a process is running."""
    try:
        subprocess.check_output(["pgrep", "-x", process_name],
                                stderr=subprocess.DEVNULL)
        return True
    except (subprocess.CalledProcessError, FileNotFoundError):
        return False


def darken(hex_color, amount=0.15):
    h = hex_color.lstrip("#")
    r, g, b = int(h[0:2], 16), int(h[2:4], 16), int(h[4:6], 16)
    f = 1.0 - amount
    return "#{:02x}{:02x}{:02x}".format(int(r * f), int(g * f), int(b * f))


def lighten(hex_color, amount=0.15):
    h = hex_color.lstrip("#")
    r, g, b = int(h[0:2], 16), int(h[2:4], 16), int(h[4:6], 16)
    r = min(255, int(r + (255 - r) * amount))
    g = min(255, int(g + (255 - g) * amount))
    b = min(255, int(b + (255 - b) * amount))
    return "#{:02x}{:02x}{:02x}".format(r, g, b)


def backup_file(filepath, backup_subdir):
    """Backup a file before modifying it."""
    if not os.path.exists(filepath):
        return
    dest_dir = os.path.join(BACKUP_DIR, backup_subdir)
    os.makedirs(dest_dir, exist_ok=True)
    dest = os.path.join(dest_dir, os.path.basename(filepath))
    shutil.copy2(filepath, dest)


def load_theme(theme_id):
    """Load a theme JSON by ID."""
    path = os.path.join(THEME_DIR, f"{theme_id}.json")
    if not os.path.exists(path):
        print(f"ERROR: Theme '{theme_id}' not found at {path}")
        sys.exit(1)
    with open(path) as f:
        return json.load(f)


def save_current(theme_id):
    """Record the currently applied theme."""
    with open(CURRENT_FILE, "w") as f:
        f.write(theme_id + "\n")


# ── Generators ────────────────────────────────────────────────────────────────

def apply_hyprland(p):
    """Generate Hyprland colors.conf from palette."""
    if not os.path.exists(os.path.dirname(HYPRLAND_COLORS)):
        return False
    backup_file(HYPRLAND_COLORS, "hyprland")
    content = f"""# Theme: {p.get('_name', 'custom')} — generated by theme-picker

$purple   = rgb({strip_hash(p['purple'])})
$pink     = rgb({strip_hash(p['pink'])})
$red      = rgb({strip_hash(p['red'])})
$orange   = rgb({strip_hash(p['orange'])})
$yellow   = rgb({strip_hash(p['yellow'])})
$green    = rgb({strip_hash(p['green'])})
$cyan     = rgb({strip_hash(p['cyan'])})
$outline  = rgb({strip_hash(p['fg_dim'])})
$error    = rgb({strip_hash(p['red'])})
$bg       = rgb({strip_hash(p['bg'])})

general {{
  col.active_border   = $purple $pink $red $orange $yellow $green $cyan 45deg
  col.inactive_border = $outline
}}

group {{
  col.border_active   = $purple $pink $red $orange $yellow $green $cyan 45deg
  col.border_inactive = $outline
  col.border_locked_active   = $error
  col.border_locked_inactive = $outline

  groupbar {{
    col.active         = $purple
    col.inactive       = $outline
    col.locked_active   = $error
    col.locked_inactive = $outline
  }}
}}
"""
    with open(HYPRLAND_COLORS, "w") as f:
        f.write(content)
    return True


